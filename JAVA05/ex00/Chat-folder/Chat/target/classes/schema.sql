-- User Table
CREATE TABLE "User" (
    id SERIAL PRIMARY KEY,                      -- Numeric identifier, generated by DBMS
    login VARCHAR(50) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL
);

-- Chatroom Table
CREATE TABLE Chatroom (
    id SERIAL PRIMARY KEY,                      -- Numeric identifier, generated by DBMS
    name VARCHAR(100) NOT NULL,
    owner_id INTEGER NOT NULL REFERENCES "User"(id)  -- Foreign key to User
);

-- Message Table
CREATE TABLE Message (
    id SERIAL PRIMARY KEY,                      -- Numeric identifier, generated by DBMS
    author_id INTEGER NOT NULL REFERENCES "User"(id), -- Foreign key to User
    chatroom_id INTEGER NOT NULL REFERENCES Chatroom(id), -- Foreign key to Chatroom
    text TEXT NOT NULL,
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- User_Chatroom Junction Table (for many-to-many relationship)
CREATE TABLE User_Chatroom (
    user_id INTEGER NOT NULL REFERENCES "User"(id),
    chatroom_id INTEGER NOT NULL REFERENCES Chatroom(id),
    PRIMARY KEY (user_id, chatroom_id)          -- Composite primary key for the junction table
);
